// Normalizer for slug matching
export function normalizeCategorySlug(s?: string): string {
  return (s ?? "")
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

export function getCategoryTip(categorySlugOrName?: string) {
  const normalized = normalizeCategorySlug(categorySlugOrName);
  return categoryEducationTips[normalized] ?? null;
}

